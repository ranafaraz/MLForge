"""Offline eval harness: measure each protocol's optimism against the oracle.

Run: ``python -m evals.harness`` (writes ``evals/RESULTS.md``).

For every dataset regime we run all three selection protocols across many seeds
and compare the *reported* cross-validation score with the *oracle* accuracy of
the shipped model. The gap is **optimism**. Two effects fall straight out of the
differences:

* **preprocessing-leakage effect** = optimism(leaky) - optimism(pipeline)
* **selection-bias effect**        = optimism(pipeline) - optimism(nested)

The ``lowdim`` regime is the control where the protocols agree (no feature
selection to leak through); the ``null`` regime is where the leaky protocol
reports accuracy well above the 0.5 truth -- a manufactured signal that nested CV
refuses to report. No API keys, no downloads.
"""

from __future__ import annotations

import json
from pathlib import Path

from evals.metrics import aggregate
from mlforge.config import Settings
from mlforge.data import make_dataset
from mlforge.selection import PROTOCOLS, run_protocol

N_SEEDS = 8
DATASETS_EVAL = ("highdim", "lowdim", "null")
RESULTS_PATH = Path(__file__).parent / "RESULTS.md"


def _run_cell(dataset: str, protocol: str) -> dict:
    results = []
    for seed in range(N_SEEDS):
        s = Settings(seed=seed, dataset=dataset, protocol=protocol)
        ds = make_dataset(dataset, s)
        results.append(run_protocol(protocol, ds, s))
    return aggregate(results)


def run_eval() -> dict:
    table = {
        dataset: {proto: _run_cell(dataset, proto) for proto in PROTOCOLS}
        for dataset in DATASETS_EVAL
    }
    bayes = {
        dataset: make_dataset(dataset, Settings(seed=0)).bayes_accuracy
        for dataset in DATASETS_EVAL
    }

    def opt(dataset, proto):
        return table[dataset][proto]["optimism"]

    return {
        "config": {"seeds": N_SEEDS, "datasets": list(DATASETS_EVAL)},
        "table": table,
        "bayes": bayes,
        # Flat scalars consumed by the gate.
        "hd_leaky_opt": opt("highdim", "leaky"),
        "hd_pipeline_opt": opt("highdim", "pipeline"),
        "hd_nested_opt": opt("highdim", "nested"),
        "hd_leakage_effect": opt("highdim", "leaky") - opt("highdim", "pipeline"),
        "hd_selection_effect": opt("highdim", "pipeline") - opt("highdim", "nested"),
        "hd_leaky_reported": table["highdim"]["leaky"]["reported"],
        "hd_pipeline_reported": table["highdim"]["pipeline"]["reported"],
        "hd_nested_reported": table["highdim"]["nested"]["reported"],
        "low_leaky_opt": opt("lowdim", "leaky"),
        "low_nested_opt": opt("lowdim", "nested"),
        "null_leaky_reported": table["null"]["leaky"]["reported"],
        "null_nested_reported": table["null"]["nested"]["reported"],
        "null_leaky_oracle": table["null"]["leaky"]["oracle"],
    }


def _fmt(m: dict) -> str:
    return (
        f"{m['reported']:.3f} | {m['oracle']:.3f} | "
        f"{m['optimism']:+.3f} (±{m['optimism_sd']:.3f})"
    )


def _render_markdown(res: dict) -> str:
    c = res["config"]
    t = res["table"]
    lines = [
        "# MLForge - Offline Evaluation Results",
        "",
        f"Generated by `python -m evals.harness` over {c['seeds']} seeds per regime, "
        "no API keys, no downloads. Each protocol's **reported** cross-validation score "
        "is compared with the **oracle** accuracy of the shipped model on a large held-out "
        "test set drawn from the same generator. The gap is **optimism**.",
        "",
        "## Headline (regime `highdim`): the same data, three honesties",
        "",
        "| Protocol | Reported | Oracle | Optimism |",
        "|---|---:|---:|---:|",
        f"| leaky (preproc on all data + best-of-grid) | {_fmt(t['highdim']['leaky'])} |",
        f"| pipeline (no leak, best-of-grid) | {_fmt(t['highdim']['pipeline'])} |",
        f"| **nested (honest)** | {_fmt(t['highdim']['nested'])} |",
        "",
        "Two effects, cleanly separated:",
        "",
        f"- **Proper pipelining buys a preprocessing-leakage correction.** "
        f"Fitting the scaler + feature selector inside each fold instead of on all the "
        f"data cuts optimism by **{res['hd_leakage_effect']:+.3f}** "
        f"(leaky {res['hd_leaky_opt']:+.3f} -> pipeline {res['hd_pipeline_opt']:+.3f}).",
        f"- **Nested cross-validation buys a selection-bias correction.** "
        f"Estimating the best-of-grid model with an outer loop instead of reporting its own "
        f"inflated flat-CV score cuts optimism by a further "
        f"**{res['hd_selection_effect']:+.3f}** "
        f"(pipeline {res['hd_pipeline_opt']:+.3f} -> nested {res['hd_nested_opt']:+.3f}), "
        f"landing within ±0.02 of the oracle.",
        "",
        "## Control (regime `lowdim`): nothing to leak, so the protocols agree",
        "",
        "| Protocol | Reported | Oracle | Optimism |",
        "|---|---:|---:|---:|",
        f"| leaky | {_fmt(t['lowdim']['leaky'])} |",
        f"| pipeline | {_fmt(t['lowdim']['pipeline'])} |",
        f"| nested | {_fmt(t['lowdim']['nested'])} |",
        "",
        "With few, all-informative features there is no high-dimensional selection step for "
        "leakage to exploit, so even the leaky protocol is honest "
        f"(optimism {res['low_leaky_opt']:+.3f}). Leakage needs a wide feature space to bite.",
        "",
        "## Null test (regime `null`): random labels, true accuracy is 0.5",
        "",
        "| Protocol | Reported | Oracle |",
        "|---|---:|---:|",
        f"| leaky | {t['null']['leaky']['reported']:.3f} | {t['null']['leaky']['oracle']:.3f} |",
        f"| pipeline | {t['null']['pipeline']['reported']:.3f} | "
        f"{t['null']['pipeline']['oracle']:.3f} |",
        f"| nested | {t['null']['nested']['reported']:.3f} | "
        f"{t['null']['nested']['oracle']:.3f} |",
        "",
        f"The labels are independent of the features, so the truth is 0.5. The leaky protocol "
        f"still reports **{res['null_leaky_reported']:.3f}** -- a signal manufactured entirely by "
        f"selecting features on the held-out rows. Nested CV reports "
        f"**{res['null_nested_reported']:.3f}**, refusing to be fooled. This collapse is the "
        "proof that the leak, not the data, produced the fake accuracy.",
        "",
        "## How to read the columns",
        "",
        "`Reported` is what you would publish; `Oracle` is the truth on held-out data; "
        "`Optimism = Reported - Oracle`. A trustworthy protocol has optimism near zero on "
        "every regime -- only `nested` does.",
        "",
    ]
    return "\n".join(lines)


def main() -> int:
    res = run_eval()
    RESULTS_PATH.write_text(_render_markdown(res), encoding="utf-8")
    print("MLForge eval complete. Wrote", RESULTS_PATH.name)
    summary = {
        "highdim": {p: res["table"]["highdim"][p] for p in PROTOCOLS},
        "leakage_effect": res["hd_leakage_effect"],
        "selection_effect": res["hd_selection_effect"],
        "null_leaky_reported": res["null_leaky_reported"],
        "null_nested_reported": res["null_nested_reported"],
    }
    print(json.dumps(summary, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
